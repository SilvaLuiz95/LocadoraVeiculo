    public void carregarModelo(int id) {
        modeloEditar = daoM.select(id);
        txtNomeModelo.setText(modeloEditar.getNome());

        List<Fabricante> fabricantes = daoF.select();
        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();

        for (Fabricante fabricante : fabricantes) {
            comboBoxModel.addElement(fabricante.getNome());
        }
        cboFabricante.setModel(comboBoxModel);
        cboFabricante.setSelectedItem(modeloEditar.getFabricante().getNome());
    }

    public void carregarComboBoxFabricante() {

        List<Fabricante> listaFabricante = daoF.select();

        Object[] valoresModel = new Object[listaFabricante.size()];

        int i = 0;

        for (Fabricante fabricante1 : listaFabricante) {
            valoresModel[i] = fabricante1;

            i++;
        }

        DefaultComboBoxModel model = new DefaultComboBoxModel(valoresModel);

        cboFabricante.setModel(model);
    }

    public void limparCampos() {
        txtNomeModelo.setText("");
        cboFabricante.setSelectedIndex(0);
    }

