    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (modeloEditar != null) {
            String nome = txtNomeModelo.getText();
            try {
                daoM.update(modeloEditar.getId(), nome);
                JOptionPane.showMessageDialog(this, "Modelo Alterado com Sucesso", "SUCESS", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        } else {
            String nome = txtNomeModelo.getText();
            Fabricante nomeFabricante = (Fabricante) cboFabricante.getSelectedItem();

            try {
                int idFabricante = nomeFabricante.getId();
                daoM.insert(nome, idFabricante);
                limparCampos();
                JOptionPane.showMessageDialog(this, "Modelo Inserido com Sucesso", "SUCESS", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

